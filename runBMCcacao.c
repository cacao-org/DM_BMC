static tD GetTOD(void)
/*
**	Get fractional seconds since the epoch.
*/
{
struct timeval	tv;
tD				ans=0.0;

	(void)gettimeofday(&tv,NULL);
	ans = tv.tv_usec;
	ans /= 1000000.0;
	ans += tv.tv_sec;

	return(ans);
}



