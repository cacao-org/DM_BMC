/*==========================================================================*/
static void MakeOpen(void)
/*
**	Make sure the library has been opened
*/
{
    tE	err;

    if (NULL != sBMC) {
        return;
    }

    if (kBMCEnoErr != (err = BMCopen(sNdx,&sBMC))) {
        ERR(("MakeOpen: %d gave %s\n",sNdx,BMCgetErrStr(err)));
        exit(1);
    }

    return;
}


